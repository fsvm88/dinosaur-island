package dinolib;


public class Coordinate extends Mappa {
	/**
	 * Contiene la coordinata x.
	 * @uml.property  name="x"
	 */
	private int x;

	/**
	 * Getter of the property <tt>x</tt>
	 * @return  Returns the x.
	 * @uml.property  name="x"
	 */
	public int getX() {
		return x;
	}

	/**
	 * Setter of the property <tt>x</tt>
	 * @param x  The x to set.
	 * @uml.property  name="x"
	 */
	public void setX(int x) {
		this.x = x;
	}

	/**
	 * Contiene la coordinata y.
	 * @uml.property  name="y"
	 */
	private int y;

	/**
	 * Getter of the property <tt>y</tt>
	 * @return  Returns the y.
	 * @uml.property  name="y"
	 */
	public int getY() {
		return y;
	}

	/**
	 * Setter of the property <tt>y</tt>
	 * @param y  The y to set.
	 * @uml.property  name="y"
	 */
	public void setY(int y) {
		this.y = y;
	}
}
